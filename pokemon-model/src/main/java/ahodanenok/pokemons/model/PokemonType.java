package ahodanenok.pokemons.model;

public class PokemonType {

    private int id;
    private String name;

    public PokemonType() { }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
